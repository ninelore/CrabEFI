///
/// This enrolls:
/// - Microsoft KEK CA 2011 -> KEK
/// - Microsoft Windows Production PCA 2011 -> db
/// - Microsoft Corporation UEFI CA 2011 -> db
/// - Default PK (Microsoft KEK CA 2011 as placeholder)
