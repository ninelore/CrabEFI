//! Framebuffer Text Console
//!
//! This module provides text rendering on the coreboot framebuffer using an
//! embedded 8x16 VGA bitmap font. It supports basic text output with colors,
//! cursor positioning, and scrolling.

use crate::coreboot::framebuffer::FramebufferInfo;
use core::fmt::{self, Write};

/// Character width in pixels
pub const CHAR_WIDTH: u32 = 8;

/// Character height in pixels
pub const CHAR_HEIGHT: u32 = 16;

/// Default foreground color (light gray)
pub const DEFAULT_FG: Color = Color::new(192, 192, 192);

/// Default background color (black)
pub const DEFAULT_BG: Color = Color::new(0, 0, 0);

/// Highlight foreground color (black)
pub const HIGHLIGHT_FG: Color = Color::new(0, 0, 0);

/// Highlight background color (cyan)
pub const HIGHLIGHT_BG: Color = Color::new(0, 192, 192);

/// Title color (yellow)
pub const TITLE_COLOR: Color = Color::new(255, 255, 0);

/// A simple RGB color
#[derive(Clone, Copy, Debug)]
pub struct Color {
    pub r: u8,
    pub g: u8,
    pub b: u8,
}

impl Color {
    /// Create a new color from RGB values
    pub const fn new(r: u8, g: u8, b: u8) -> Self {
        Color { r, g, b }
    }

    /// White color
    pub const fn white() -> Self {
        Color::new(255, 255, 255)
    }

    /// Black color
    pub const fn black() -> Self {
        Color::new(0, 0, 0)
    }
}

/// Framebuffer text console
pub struct FramebufferConsole<'a> {
    /// Reference to the framebuffer info
    fb: &'a FramebufferInfo,
    /// Current cursor column (0-based)
    cursor_col: u32,
    /// Current cursor row (0-based)
    cursor_row: u32,
    /// Number of columns (characters per line)
    cols: u32,
    /// Number of rows (lines on screen)
    rows: u32,
    /// Current foreground color
    fg_color: Color,
    /// Current background color
    bg_color: Color,
}

impl<'a> FramebufferConsole<'a> {
    /// Create a new framebuffer console
    ///
    /// # Arguments
    ///
    /// * `fb` - Reference to the framebuffer info from coreboot
    pub fn new(fb: &'a FramebufferInfo) -> Self {
        let cols = fb.x_resolution / CHAR_WIDTH;
        let rows = fb.y_resolution / CHAR_HEIGHT;

        FramebufferConsole {
            fb,
            cursor_col: 0,
            cursor_row: 0,
            cols,
            rows,
            fg_color: DEFAULT_FG,
            bg_color: DEFAULT_BG,
        }
    }

    /// Get the number of columns (characters per line)
    pub fn cols(&self) -> u32 {
        self.cols
    }

    /// Get the number of rows (lines on screen)
    pub fn rows(&self) -> u32 {
        self.rows
    }

    /// Set the cursor position
    pub fn set_position(&mut self, col: u32, row: u32) {
        self.cursor_col = col.min(self.cols.saturating_sub(1));
        self.cursor_row = row.min(self.rows.saturating_sub(1));
    }

    /// Get the current cursor position
    pub fn position(&self) -> (u32, u32) {
        (self.cursor_col, self.cursor_row)
    }

    /// Set the foreground and background colors
    pub fn set_colors(&mut self, fg: Color, bg: Color) {
        self.fg_color = fg;
        self.bg_color = bg;
    }

    /// Set just the foreground color
    pub fn set_fg_color(&mut self, fg: Color) {
        self.fg_color = fg;
    }

    /// Set just the background color
    pub fn set_bg_color(&mut self, bg: Color) {
        self.bg_color = bg;
    }

    /// Reset colors to default
    pub fn reset_colors(&mut self) {
        self.fg_color = DEFAULT_FG;
        self.bg_color = DEFAULT_BG;
    }

    /// Clear the entire screen with the current background color
    pub fn clear(&mut self) {
        unsafe {
            self.fb
                .clear(self.bg_color.r, self.bg_color.g, self.bg_color.b);
        }
        self.cursor_col = 0;
        self.cursor_row = 0;
    }

    /// Clear a single line
    pub fn clear_line(&mut self, row: u32) {
        if row >= self.rows {
            return;
        }

        let y_start = row * CHAR_HEIGHT;
        for y in y_start..(y_start + CHAR_HEIGHT) {
            for x in 0..self.fb.x_resolution {
                unsafe {
                    self.fb
                        .write_pixel(x, y, self.bg_color.r, self.bg_color.g, self.bg_color.b);
                }
            }
        }
    }

    /// Write a single character at the current cursor position
    pub fn put_char(&mut self, c: char) {
        match c {
            '\n' => {
                self.cursor_col = 0;
                self.cursor_row += 1;
                if self.cursor_row >= self.rows {
                    self.scroll_up();
                    self.cursor_row = self.rows - 1;
                }
            }
            '\r' => {
                self.cursor_col = 0;
            }
            '\t' => {
                // Tab to next 8-column boundary
                let next_tab = (self.cursor_col + 8) & !7;
                while self.cursor_col < next_tab && self.cursor_col < self.cols {
                    self.draw_char(' ');
                    self.cursor_col += 1;
                }
                if self.cursor_col >= self.cols {
                    self.cursor_col = 0;
                    self.cursor_row += 1;
                    if self.cursor_row >= self.rows {
                        self.scroll_up();
                        self.cursor_row = self.rows - 1;
                    }
                }
            }
            '\x08' => {
                // Backspace
                if self.cursor_col > 0 {
                    self.cursor_col -= 1;
                }
            }
            _ => {
                self.draw_char(c);
                self.cursor_col += 1;
                if self.cursor_col >= self.cols {
                    self.cursor_col = 0;
                    self.cursor_row += 1;
                    if self.cursor_row >= self.rows {
                        self.scroll_up();
                        self.cursor_row = self.rows - 1;
                    }
                }
            }
        }
    }

    /// Draw a character at the current cursor position (without advancing)
    fn draw_char(&self, c: char) {
        render_glyph(
            self.fb,
            c,
            self.cursor_col * CHAR_WIDTH,
            self.cursor_row * CHAR_HEIGHT,
            self.fg_color,
            self.bg_color,
        );
    }

    /// Draw a character at a specific position (without affecting cursor)
    pub fn draw_char_at(&self, c: char, col: u32, row: u32, fg: Color, bg: Color) {
        if col >= self.cols || row >= self.rows {
            return;
        }
        render_glyph(self.fb, c, col * CHAR_WIDTH, row * CHAR_HEIGHT, fg, bg);
    }

    /// Scroll the screen up by one line
    pub fn scroll_up(&mut self) {
        // This is a simple but slow implementation - copy pixel by pixel
        // A faster version would use memcpy on scanlines
        let bytes_per_pixel = (self.fb.bits_per_pixel / 8) as usize;
        let line_height = CHAR_HEIGHT as usize;
        let scanline_bytes = self.fb.bytes_per_line as usize;
        let copy_height = ((self.rows - 1) * CHAR_HEIGHT) as usize;

        unsafe {
            let fb_ptr = self.fb.as_ptr();

            // Copy each scanline up by one character height
            for y in 0..copy_height {
                let src_offset = (y + line_height) * scanline_bytes;
                let dst_offset = y * scanline_bytes;
                let line_bytes = self.fb.x_resolution as usize * bytes_per_pixel;

                core::ptr::copy(fb_ptr.add(src_offset), fb_ptr.add(dst_offset), line_bytes);
            }
        }

        // Clear the last line
        self.clear_line(self.rows - 1);
    }

    /// Write a horizontal line of box-drawing characters
    pub fn draw_horizontal_line(
        &mut self,
        row: u32,
        left_char: char,
        fill_char: char,
        right_char: char,
    ) {
        if row >= self.rows {
            return;
        }

        self.set_position(0, row);
        self.put_char(left_char);
        for _ in 1..(self.cols - 1) {
            self.put_char(fill_char);
        }
        self.put_char(right_char);
    }

    /// Write a string centered on a line
    pub fn write_centered(&mut self, row: u32, s: &str) {
        if row >= self.rows {
            return;
        }

        let len = s.chars().count() as u32;
        let col = if len < self.cols {
            (self.cols - len) / 2
        } else {
            0
        };

        self.set_position(col, row);
        s.chars().for_each(|c| self.put_char(c));
    }
}

impl<'a> Write for FramebufferConsole<'a> {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        s.chars().for_each(|c| self.put_char(c));
        Ok(())
    }
}

/// Render a character glyph to a framebuffer at the given pixel coordinates.
pub fn render_glyph(fb: &FramebufferInfo, c: char, x: u32, y: u32, fg: Color, bg: Color) {
    let glyph = get_glyph(c);
    for row in 0..CHAR_HEIGHT {
        let bits = glyph[row as usize];
        for col in 0..CHAR_WIDTH {
            let pixel_set = (bits >> (7 - col)) & 1 != 0;
            let (r, g, b) = if pixel_set {
                (fg.r, fg.g, fg.b)
            } else {
                (bg.r, bg.g, bg.b)
            };
            unsafe {
                fb.write_pixel(x + col, y + row, r, g, b);
            }
        }
    }
}

/// Get the glyph data for a character
///
/// Returns a reference to 16 bytes representing the 8x16 bitmap for the character.
fn get_glyph(c: char) -> &'static [u8; 16] {
    let index = c as usize;
    if index < 256 {
        &VGA_FONT_8X16[index]
    } else {
        // Return '?' for non-ASCII characters
        &VGA_FONT_8X16[b'?' as usize]
    }
}

